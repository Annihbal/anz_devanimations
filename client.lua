Citizen.CreateThread(function()
    while true do
        Citizen.Wait(0)
        if IsControlJustReleased(0, 0x156F7119) then
            VORPMenu.CloseAll()
        end
    end
end)

function OpenMenu()

	VORPMenu.CloseAll()
   
	   local MenuElements = {}
   
	   for i, animation in ipairs(animpostfx) do
		   table.insert(MenuElements, { 
			   label = animation,
			   value = animation,
			   desc = "Cliquez pour jouer l'animation " .. animation
		   })
	   end
	
	   VORPMenu.Open("default",GetCurrentResourceName(),"vorp_menu_OpenMenu",
   
	   {
		   title = "Menu d'animations",
		   subtext = "Choisissez une animation",
		   align = "top-right", 
		   elements = MenuElements, 
	   },
   
   
	   function(data, menu)
		   if data.current.value then
			   AnimpostfxStopAll()
			   AnimpostfxPlay(data.current.value)
		   end
	   end)
   
end



RegisterCommand('devanime', function()
    OpenMenu()
end, false)

RegisterCommand('closemenu', function()
    VORPMenu.CloseAll()
end, false)

